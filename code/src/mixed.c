/**
 * Encode one frame worth of audio to the output file.
 * @param      frame                 Samples to be encoded
 * @param      output_format_context Format context of the output file
 * @param      output_codec_context  Codec context of the output file
 * @param[out] data_present          Indicates whether data has been
 *                                   decoded
 * @return Error code (0 if successful)
 */
static int encode_audio_frame(AVFrame *frame,
                              AVFormatContext *output_format_context,
                              AVCodecContext *output_codec_context,
                              int *data_present)
{
    /* Packet used for temporary storage. */
    AVPacket output_packet;
    int error;
    init_packet(&output_packet);
 
    /* Set a timestamp based on the sample rate for the container. */

    frame->pts = frame->best_effort_timestamp;
    /* Encode the audio frame and store it in the temporary packet.
     * The output audio stream encoder is used to do this. */
    if ((error = avcodec_encode_audio2(output_codec_context, &output_packet,
                                       frame, data_present)) < 0) {
        
        av_packet_unref(&output_packet);
        return error;
    }
    
    if (*data_present) {
        if ((error = av_write_frame(output_format_context, &output_packet)) < 0) {
           
            av_packet_unref(&output_packet);
            return error;
        }
        
        av_packet_unref(&output_packet);
    }

    return 0;
}

/**
 * Load one audio frame from the FIFO buffer, encode and write it to the
 * output file.
 * @param fifo                  Buffer used for temporary storage
 * @param output_format_context Format context of the output file
 * @param output_codec_context  Codec context of the output file
 * @return Error code (0 if successful)
 */
static int load_encode_and_write(AVAudioFifo *fifo,
                                 AVFormatContext *output_format_context,
                                 AVCodecContext *output_codec_context)
{
    /* Temporary storage of the output samples of the frame written to the file. */
    AVFrame *output_frame;
    /* Use the maximum number of possible samples per frame.
     * If there is less than the maximum possible frame size in the FIFO
     * buffer use this number. Otherwise, use the maximum possible frame size. */
    const int frame_size = FFMIN(av_audio_fifo_size(fifo),
                                 output_codec_context->frame_size);
    int data_written;

    /* Initialize temporary storage for one output frame. */
    if (init_output_frame(&output_frame, output_codec_context, frame_size))
        return AVERROR_EXIT;

    /* Read as many samples from the FIFO buffer as required to fill the frame.
     * The samples are stored in the frame temporarily. */
    if (av_audio_fifo_read(fifo, (void **)output_frame->data, frame_size) < frame_size) {
        fprintf(stderr, "Could not read data from FIFO\n");
        av_frame_free(&output_frame);
        return AVERROR_EXIT;
    }

    /* Encode one frame worth of audio samples. */
    if (encode_audio_frame(output_frame, output_format_context,
                           output_codec_context, &data_written)) {
        av_frame_free(&output_frame);
        return AVERROR_EXIT;
    }
    av_frame_free(&output_frame);
    return 0;
}

/**
 * Write the trailer of the output file container.
 * @param output_format_context Format context of the output file
 * @return Error code (0 if successful)
 */
static int write_output_file_trailer(AVFormatContext *output_format_context)
{
    int error;
    if ((error = av_write_trailer(output_format_context)) < 0) {
        fprintf(stderr, "Could not write output file trailer (error '%s')\n",
                av_err2str(error));
        return error;
    }
    return 0;
}
int main(int argc, char **argv)
{
    int ret;
    int pt=0;
    AVPacket packet = { .data = NULL, .size = 0 };
    AVFrame *frame = NULL;
    SwrContext *resample_context = NULL;
    AVAudioFifo *fifo = NULL;
    enum AVMediaType type;
    unsigned int stream_index;
    unsigned int i;
    int got_frame;
    int (*dec_func)(AVCodecContext *, AVFrame *, int *, const AVPacket *);
    if (argc != 3) {
        av_log(NULL, AV_LOG_ERROR, "Usage: %s <input file> <output file>\n", argv[0]);
        return 1;
    }
    av_register_all();
	avfilter_register_all();
	
    if ((ret = open_input_file(argv[1])) < 0)
        goto end;
   
    if ((ret = open_output_file(argv[2])) < 0)
        goto end;
	
    if ((ret = init_filters()) < 0)
        goto end;
   
    if(voice_stream >= 0){
        if (init_resampler(ifmt_ctx->streams[voice_stream]->codec,ofmt_ctx->streams[voice_stream]->codec, &resample_context))
             goto end;
        /** Initialize the FIFO buffer to store audio samples to be encoded. */
      
        if (init_fifo(&fifo,ofmt_ctx->streams[voice_stream]->codec))
            goto end;
    }
  
    while (1) {

        init_packet(&packet);

        if ((ret = av_read_frame(ifmt_ctx, &packet)) < 0)
            break;
        pt++;
        stream_index = packet.stream_index;
        
        type = ifmt_ctx->streams[packet.stream_index]->codec->codec_type;
       
    if(type == AVMEDIA_TYPE_VIDEO){

         av_log(NULL, AV_LOG_INFO, "Demuxer gave frame of stream_index %u\n",
                stream_index);
   
       
        if (filter_ctx[stream_index].filter_graph) {

            av_log(NULL, AV_LOG_DEBUG, "Going to reencode&filter the frame\n");
            
            frame = av_frame_alloc();
            
            if (!frame) {
                ret = AVERROR(ENOMEM);
                break;
            }
            packet.pts=pt;//packet.pts-ifmt_ctx->streams[stream_index]->start_time;

            av_packet_rescale_ts(&packet,
                                 ifmt_ctx->streams[stream_index]->time_base,
                                 ofmt_ctx->streams[stream_index]->codec->time_base);

            dec_func =  avcodec_decode_video2;
            ret = dec_func(ifmt_ctx->streams[stream_index]->codec, frame,
                    &got_frame, &packet);
            if (ret < 0) {
                av_frame_free(&frame);
                av_log(NULL, AV_LOG_ERROR, "Decoding failed\n");
                break;
            }
           
            if (got_frame) {
                frame->pts = frame->best_effort_timestamp;
                ret = filter_encode_write_frame(frame, stream_index);
                av_frame_free(&frame);
                if (ret < 0)
                    goto end;
            } else {
                av_frame_free(&frame);
            }
        } else {
            /* remux this frame without reencoding */
            av_packet_rescale_ts(&packet,
                                 ifmt_ctx->streams[stream_index]->time_base,
                                 ofmt_ctx->streams[stream_index]->time_base);

            ret = av_interleaved_write_frame(ofmt_ctx, &packet);
            if (ret < 0)
                goto end;
        }
        av_packet_unref(&packet);
    }
//audio part!!!!!!!!!!!!!!!
        else if(type == AVMEDIA_TYPE_AUDIO){
    
            
            /** Use the encoder's desired frame size for processing. */
            const int output_frame_size =  ofmt_ctx->streams[voice_stream]->codec->frame_size;
          
            int finished                = 0;
            /**
             * Make sure that there is one frame worth of samples in the FIFO
             * buffer so that the encoder can do its work.
             * Since the decoder's and the encoder's frame size may differ, we
             * need to FIFO buffer to store as many frames worth of input samples
             * that they make up at least one frame worth of output samples.
             */
            av_packet_rescale_ts(&packet,
                                 ifmt_ctx->streams[voice_stream]->time_base,
                                 ofmt_ctx->streams[voice_stream]->codec->time_base);
            if  (av_audio_fifo_size(fifo) < output_frame_size) {
              
                /**
                 * Decode one frame worth of audio samples, convert it to the
                 * output sample format and put it into the FIFO buffer.
                 */
                if (read_decode_convert_and_store(fifo, 
                                                ifmt_ctx->streams[voice_stream]->codec,
                                                ofmt_ctx->streams[voice_stream]->codec,
                                                resample_context, &finished, &packet))
                    break;
                
                /**
                 * If we are at the end of the input file, we continue
                 * encoding the remaining audio samples to the output file.
                 */
                if (finished)
                    break;

            }
            /**
             * If we have enough samples for the encoder, we encode them.
             * At the end of the file, we pass the remaining samples to
             * the encoder.
             */
            while (av_audio_fifo_size(fifo) >= output_frame_size ||
                (finished && av_audio_fifo_size(fifo) > 0))
                /**
                 * Take one frame worth of audio samples from the FIFO buffer,
                 * encode it and write it to the output file.
                 */
                if (load_encode_and_write(fifo, ofmt_ctx,
                                         ofmt_ctx->streams[voice_stream]->codec))
                    break;
        
            /**
             * If we are at the end of the input file and have encoded
             * all remaining samples, we can exit this loop and finish.
             */
            /*
            if (finished) {
                int data_written;
               
                do {
                    if (encode_audio_frame(NULL, ofmt_ctx,
                                         ofmt_ctx->streams[1]->codec, &data_written))
                        continue;
                } while (data_written);
                break;
            }
            */
        }
    }
    /* flush filters and encoders */
    for (i = 0; i < ifmt_ctx->nb_streams; i++) {
        /* flush filter */
       
        if (!filter_ctx[i].filter_graph)
            continue;
        ret = filter_encode_write_frame(NULL, i);
        if (ret < 0) {
            av_log(NULL, AV_LOG_ERROR, "Flushing filter failed\n");
            goto end;
        }
        /* flush encoder */
        ret = flush_encoder(i);
        if (ret < 0) {
            av_log(NULL, AV_LOG_ERROR, "Flushing encoder failed\n");
            goto end;
        }
        
    }
    av_write_trailer(ofmt_ctx);
end:
    av_free_packet(&packet);
    av_frame_free(&frame);
    if (fifo)
        av_audio_fifo_free(fifo);
    swr_free(&resample_context);
    for (i = 0; i < ifmt_ctx->nb_streams; i++) {
        avcodec_close(ifmt_ctx->streams[i]->codec);
        if (ofmt_ctx && ofmt_ctx->nb_streams > i && ofmt_ctx->streams[i] && ofmt_ctx->streams[i]->codec)
            avcodec_close(ofmt_ctx->streams[i]->codec);
        if (filter_ctx && filter_ctx[i].filter_graph)
            avfilter_graph_free(&filter_ctx[i].filter_graph);
    }
    av_free(filter_ctx);
    avformat_close_input(&ifmt_ctx);
    if (ofmt_ctx && !(ofmt_ctx->oformat->flags & AVFMT_NOFILE))
        avio_close(ofmt_ctx->pb);
    avformat_free_context(ofmt_ctx);
    if (ret < 0)
        av_log(NULL, AV_LOG_ERROR, "Error occurred: %s\n", av_err2str(ret));
    return ret ? 1 : 0;
}

